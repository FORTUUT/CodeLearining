#2.3 Python的数据类型测试
x1=0;   print("x1的值是",x1,",类型是整型",type(x1))
x2=0.0;   print("x2的值是",x2,",类型是浮点型",type(x2))
x3=0.0+0.0j;   print("x3的值是",x3,",类型是复数型",type(x3))
x4=False;    print("x4的值是",x4,",类型是布尔型",type(x4))
x5="Python";   print("x5的值是",x5,",类型是字符串型",type(x5))
x6=[3,4,5,"three"];   print("x6的值是",x6,",类型是列表型",type(x6))
x7=('physics','chemistry',2000);   print("x7的值是",x7,",类型是元组型",type(x7))
x8={'AU':'Austaria','CN':'China'};   print("x8的值是",x8,",类型是字典型",type(x8))
x9=set([1,2,3,4]);   print("x9的值是",x9,",类型是集合型",type(x9))

