#2.3 布尔型（测试和计算）
#布尔型测试
x1=0;   print(type(x1),bool(x1))
x2=0.0;   print(type(x2),bool(x2))
x3=0.0+0.0j;   print(type(x3),bool(x3))
x4=False;    print(type(x4),bool(x4))
x5="";   print(type(x5),bool(x5))
x6=[];   print(type(x6),bool(x6))
x7=();   print(type(x7),bool(x7))
x8={};   print(type(x8),bool(x8))
x9=set();  print(type(x9),bool(x9))
#布尔型计算
x10=False+True;   print(x10,type(x10))
x11=True+True;    print(x11,type(x11))
