#3.2 字符串的格式化（format方法格式化）
#例3-4 使用str.format()方法格式化字符串
print('{:*>8}'.format('3.14'))  #宽度8位，右对齐
print('{:*<8}'.format('3.14')) #宽度8位，左对齐
print('{0:^8},{0:*^8}'.format('3.14')) #宽度8位，居中对齐
print('{0:^+8.2f},{0:^-8.2f}'.format(-3.14)) #宽度8位，居中对齐
print('{0:^10.6f},{0:^10.6f}'.format(3.1415926)) #宽度10位，居中对齐
print('{0:e},{0:.2e}'.format(3.14159)) #科学计数法表示
print('{0:.2%},{0:.0%}'.format(0.314)) #输出浮点数的百分比表示